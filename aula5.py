# Tipo de de dado bool (boolean)
# Ao questionar algo em um programa,
# só existem duas respostas possíveis:
# Sim (True) ou não (False).
# Existem varios operadores para "questionar".
# dentre eles o ==, que um operador lógico que
# questiona se um valor é igual a outro. -><- <> 

print(10 == 10)  # Sim -> True (verdadeiro).
print(10 != 10)  # Não -> False (falso).
print(10 >=15)  # Sim -> True (verdadeiro).
print(10 <= 15)  # Não -> False (falso).
print(type(1 < 2))  # Boolean